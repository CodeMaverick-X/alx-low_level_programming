#include "3-calc.h"
/**
 * op_add - addition
 * @a: a
 * @b: b
 * Return: intiger
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - subtraction
 * @a: a
 * @b: b
 * Return: intiger
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul -multiply
 * @a: a
 * @b: b
 * Return: intiger
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - division
 * @a: a
 * @b: b
 * Return: intiger
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - modulos
 * @a: a
 * @b: b
 * Return: intiger
 */
int op_mod(int a, int b)
{
	return (a % b);
}
